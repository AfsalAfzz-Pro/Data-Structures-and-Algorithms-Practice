# THIS IS A PROGRAM TO FIND THE SECOND LAST VALUE IN A LINKEDLIST

class Node:
    def __init__(self, value):
        self.value = value
        self.next = None


class LinkedList:
    def __init__(self, value):
        new_node = Node(value)
        self.head = new_node
        self.tail = new_node
        self.length = 1

    def print(self):
        temp = self.head
        while temp is not None:
            print(temp.value)
            temp = temp.next

    def append(self, value):
        new_node = Node(value)
        if self.head is None:
            self.head = new_node
            self.tail = new_node
        else:
            self.tail.next = new_node
            self.tail = new_node
        self.length += 1

    def find_second_last(self):
        slow = self.head
        fast = self.head
        while fast.next is not None:
            slow = fast
            fast = fast.next
        return slow.value


my_linked_list = LinkedList(1)
my_linked_list.append(2)
